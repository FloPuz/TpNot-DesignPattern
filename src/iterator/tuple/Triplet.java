package iterator.tuple;

public class Triplet {
    private final String a;
    private final String b;
    private final String c;

    public Triplet(String a, String b, String c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public String getA() {
        return a;
    }

    public String getB() {
        return b;
    }

    public String getC() {
        return c;
    }
}

